package com.example.a06.trabalhoandroid;

public class Cor {

    private String nome;
    private int id;

    public Cor(String nome, int id)
    {
        this.nome = nome;
        this.id = id;
    }

    public int getId()
    {
        return id;
    }

    public String getNome() {
        return nome;
    }
}
